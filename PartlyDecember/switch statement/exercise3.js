// Exercise 3: If, Else If, Else
// Create a variable called temperature.
// If the temperature is greater than 30, print "It is hot".
// Else if the temperature is between 20 and 30, print "The weather is warm".

// Else, print "It is cold".

let temperature = 60;

if (temperature > 30) {
  console.log("It is hot");
} else if (temperature >= 20 && temperature <= 30) {
  console.log("The weather is warm");
} else console.log("It is cold");
